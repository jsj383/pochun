# ----------------------------------------------------------
# 마르코프 체인 AI 모델 만들기
#
# 단어 다음에 어떤 단어가 자주 오는지 학습한다.
# ----------------------------------------------------------

model = {}

# 모든 문장을 학습
for sentence in train_sentences:

    words = sentence.split()

    # 문장의 마지막을 표시하기 위해 END 추가
    words.append("END")

    # 첫 단어 저장
    start = words[0]

    # START라는 시작점 생성
    if "START" not in model:
        model["START"] = []

    model["START"].append(start)

    # 단어 연결 학습
    for i in range(len(words)-1):

        current = words[i]
        next_word = words[i+1]

        if current not in model:
            model[current] = []

        model[current].append(next_word)


# ----------------------------------------------------------
# AI가 새로운 문장을 생성하는 함수
# ----------------------------------------------------------

def make_sentence():

    # START 대신 모델에 있는 아무 단어나 시작 단어로 선택
    current = random.choice(model["START"])

    result = [current]

    # 문장 길이를 6~12단어 정도로 랜덤하게 생성
    max_length = random.randint(6, 15)

    while len(result) < max_length:

        # 다음 단어가 없으면 종료
        if current not in model:
            break

        current = random.choice(model[current])

        # END를 만나면 종료
        if current == "END":
            break

        result.append(current)

    sentence = " ".join(result)

    # 첫 글자만 대문자 대신 그대로 사용
    return sentence


